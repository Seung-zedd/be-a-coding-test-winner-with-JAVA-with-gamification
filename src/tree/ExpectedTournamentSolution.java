package tree;

/*
[재귀 깨달음 노트 정리]
1. F(n)의 정의: 이 함수가 궁극적으로 나에게 퉤!(return을 의미)하고 뱉어줄 결과값의 정체가 무엇인가?(개수? 총합? 라운드 수?)
예상 대진표 함수인 solution(n, a, b): a와 b가 만날 때까지 걸리는 총 라운드 수
(∵문제 지문에서 "처음 라운드에서 A번을 가진 참가자는 경쟁자로 생각하는 B번 참가자와 몇 번째 라운드에서 만나는지 return 하는 solution 함수")

2. 상태 (base condition): 언제 이 굴레(위임)를 끝낼 것인가? 재귀가 바닥을 치는 순간(더 이상 쪼갤 수 없는 최소 단위)
예: "a와 b의 다음 라운드 번호가 같을 때(즉, 이번에 맞붙을 때), 더 이상 남은 라운드는 없고 이번 1경기로    
끝난다." -> if ((a+1)/2 == (b+1)/2) return 1;

3. ?의 정체 (The Current Contribution / 현재의 기여분): ?는 현재 내 상태를 의미함
  - 정확히는 "현재 1스텝에서 내가 (정의된 목표를 향해) 기여할 수 있는 행동/값"
  - 예: 전체 라운드 수(F(n))는 모르겠지만, 지금 내가 여기서 1경기를 치렀으니 전체 라운드 수에 1만큼 기여했다. -> 1 + ...

4. F(next)의 정체 (The Delegation / 위임): "F(next)는 다음의 상태로 위임시키는 거야?"
  - 내 몫(?)은 챙겼으니, 나머지 미지의 영역은 형태만 바꾼(Next) 똑같은 함수에게 온전히 떠넘겨 버리는 것입니다.
    나는 더 이상 신경 쓰지 않습니다.
  - 예: 내 몫으로 1라운드를 챙겼으니(?의 정체), 다음 라운드 번호로 바뀐 애들 (a+1)/2, (b+1)/2이 만나는 건 다음 함수 네가 알아서 계산해서 가져와! -> solution((a+1)/2, (b+1)/2)

💡 완전한 조립: F(now) = ?(내 몫) + F(next)(나머지 네 몫)

*/

public class ExpectedTournamentSolution {
    /*
     * [문제 26] 예상 대진표 (Expected Tournament)
     * - 지문 분석 내용이 주석으로 남아 있어야 합니다. (초고속 백지 복습 룰)
     */
    public int solution(int n, int a, int b) {
        //? solution()의 정의: a와 b는 도대체 몇 라운드에 만날 것인가?
        // base condition
        // a와 b의 다음 라운드 번호가 같을 때(즉, 이번에 맞붙을 때), 더 이상 남은 라운드는 없고 이번 1경기로 끝난다.
        if ((a + 1) / 2 == (b + 1) / 2) {
            return 1;
        }

        // ?의 정체: ?는 현재 내 상태를 의미함, 즉 전체적인 F(n)은 모르겠고 지금 내가 여기서 1경기를 치렀으니 전체 라운드 수에 1만큼 기여했다. 
        // F(next)의 정체: 다음 라운드 번호로 바뀐 애들 (a+1)/2, (b+1)/2이 만나는 건 다음 함수 네가 알아서 계산해서 가져와!
        return 1 + solution(n, (a + 1) / 2, (b + 1) / 2); 
    }
}
